package pack1;



public class Student {

	public String Name;
	public String Age;
	public String Sex;

	public void setName(String Name) {
		this.Name = Name;
	}

	public String getName() {
		return Name;
	}

	public void setAge(String Age) {
		this.Age = Age;
	}

	public String getAge() {
		return Age;
	}

	public void setSex(String Sex) {
		this.Sex = Sex;
	}

	public String getSex() {
		return Sex;
	}

}
